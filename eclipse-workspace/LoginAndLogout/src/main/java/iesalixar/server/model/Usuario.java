package iesalixar.server.model;

import java.io.Serializable;

public class Usuario implements Serializable{

	private String nombre;

	public Usuario() {
		super();
		this.nombre = nombre;
	}


	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	
	
}
